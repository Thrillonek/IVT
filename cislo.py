cislo = int(input("Zadej číslo: "))
if cislo > 0: #Kontroluje, zda je číslo větší než nula (kladné)
    print("Číslo je kladné.")
if cislo < 0: #Kontroluje, zda je číslo menší než nula (záporné)
    print("Číslo je záporné.")
if cislo == 0: #Kontroluje, zda je číslo nula
    print("Číslo je nula.")
